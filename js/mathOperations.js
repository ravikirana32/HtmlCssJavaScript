//The max() method returns the number with the highest value.
//Match.max(2,3,4,10,5,20.....)
console.log("\n---------------------  max()  --------------");
console.log(Math.max(2,3,4,10,5,20));//20


//The min() method returns the number with the lowest value.
//Match.min(2,3,4,10,5,20.....)
